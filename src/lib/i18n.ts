// Detect browser language
const detectLanguage = (): string => {
  const savedLang = localStorage.getItem('ui_language');
  if (savedLang && UI_LANGUAGES.find(l => l.code === savedLang)) {
    return savedLang;
  }
  
  const browserLang = navigator.language.split('-')[0];
  const supported = UI_LANGUAGES.find(l => l.code === browserLang);
  return supported ? browserLang : 'en';
};

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: detectLanguage(),
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false,
    },
  });

export const changeUILanguage = (langCode: string) => {
  localStorage.setItem('ui_language', langCode);
  i18n.changeLanguage(langCode);
};

